package modelo.vo;

public class AlumnoVo {
	
	private Integer codAlumno;
	private String nombreAlumno;
	private Integer edadAlumno;
	private String escuelaAlumno;
	private Integer telefonoAlumno;
	
	/**
	 * @return the codAlumno
	 */
	public Integer getCodAlumno() {
		return codAlumno;
	}
	/**
	 * @param codAlumno the codAlumno to set
	 */
	public void setCodAlumno(Integer codAlumno) {
		this.codAlumno = codAlumno;
	}
	/**
	 * @return the nombreAlumno
	 */
	public String getNombreAlumno() {
		return nombreAlumno;
	}
	/**
	 * @param nombreAlumno the nombreAlumno to set
	 */
	public void setNombreAlumno(String nombreAlumno) {
		this.nombreAlumno = nombreAlumno;
	}
	/**
	 * @return the edadAlumno
	 */
	public Integer getEdadAlumno() {
		return edadAlumno;
	}
	/**
	 * @param edadAlumno the edadAlumno to set
	 */
	public void setEdadAlumno(Integer edadAlumno) {
		this.edadAlumno = edadAlumno;
	}
	/**
	 * @return the escuelaAlumno
	 */
	public String getEscuelaAlumno() {
		return escuelaAlumno;
	}
	/**
	 * @param escuelaAlumno the escuelaAlumno to set
	 */
	public void setEscuelaAlumno(String escuelaAlumno) {
		this.escuelaAlumno = escuelaAlumno;
	}
	/**
	 * @return the telefonoAlumno
	 */
	public Integer getTelefonoAlumno() {
		return telefonoAlumno;
	}
	/**
	 * @param telefonoAlumno the telefonoAlumno to set
	 */
	public void setTelefonoAlumno(Integer telefonoAlumno) {
		this.telefonoAlumno = telefonoAlumno;
	}
	

}
